let trafficLight = "amarelo";

switch(trafficLight){
    case "vermelho":
        console.log("PARE. O Sinal está VERMELHO");
        break;

    case "amarelo":
        console.log("ATENÇÃO. O Sinal está AMARELO");
        break;

    case "verde":
        console.log("SIGA. O Sinal está VERDE");
        break;

    default:
        console.log("Sinal inválido");
        break;
}